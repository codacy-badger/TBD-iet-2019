package com.complexible.common.beans;

public class CustomClass {
    String name;
    int age;
    String designation;
    double salary;
    boolean active;

    public CustomClass() {
    }

    public CustomClass(String name, int age, String designation, double salary, boolean active) {
        this.name = name;
        this.age = age;
        this.designation = designation;
        this.salary = salary;
        this.active = active;
    }
}
